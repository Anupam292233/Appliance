package com.ApplianceUsageAPI.entities;

public class Appliance {
    private int lights;
    private int fans;
    private int fridge;
    private int washingMachine;
    private int ironBox;
    private int computers;
    private int mobilePhones;
	public int getLights() {
		return lights;
	}
	public void setLights(int lights) {
		this.lights = lights;
	}
	public int getFans() {
		return fans;
	}
	public void setFans(int fans) {
		this.fans = fans;
	}
	public int getFridge() {
		return fridge;
	}
	public void setFridge(int fridge) {
		this.fridge = fridge;
	}
	public int getWashingMachine() {
		return washingMachine;
	}
	public void setWashingMachine(int washingMachine) {
		this.washingMachine = washingMachine;
	}
	public int getIronBox() {
		return ironBox;
	}
	public void setIronBox(int ironBox) {
		this.ironBox = ironBox;
	}
	public int getComputers() {
		return computers;
	}
	public void setComputers(int computers) {
		this.computers = computers;
	}
	public int getMobilePhones() {
		return mobilePhones;
	}
	public void setMobilePhones(int mobilePhones) {
		this.mobilePhones = mobilePhones;
	}
    
    
    
}
